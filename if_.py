# if Statement

x = 10
if x < 50:
    print("x is less than 50")
print("End of the program")

'''short hand if statement
syntax: if condition: # statement 
'''
x = 10
if x < 50:    print("x is less than 50")
print("End of the program")


#Nested if statements

x = 10
if x < 50:
    if x == 10:
        print("x  is equal to 10")
    print("x is less than 50")
print("End of the program")